	TOKEN_FALSE
	TOKEN_FOR
	TOKEN_FUNCTION
	TOKEN_INT
	TOKEN_IF
	TOKEN_LET
	TOKEN_LOCAL
	TOKEN_MATCH
	TOKEN_MUTAMPERSAND
	TOKEN_MUT
	TOKEN_MOD
	TOKEN_NULL
	TOKEN_OR
	TOKEN_RETURN
	TOKEN_STRUCT
	TOKEN_TRUE
	TOKEN_TYPE
	TOKEN_USE
	TOKEN_WHILE
	TOKEN_YIELD

	TOKEN_EXCLAMATION
	TOKEN_SEMICOLON
	TOKEN_AMPERSAND
	TOKEN_BACKSLASH
